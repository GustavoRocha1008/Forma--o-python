#[].pop - sado para remover e retornar o último elemento de uma lista. No entanto, você também pode especificar um índice como argumento para remover e retornar um elemento de uma posição específica na lista.
liguangens = ["python","js","C","Java","csharp"]
print(liguangens.pop()) #csharp
# Remove e retorna o elemento na posição -1 ("Java")
print(liguangens.pop()) #java
# Remove e retorna o elemento na posição -1 ("C")
print(liguangens.pop()) #C
# Remove e retorna o elemento na posição -1 ("js")
print(liguangens.pop()) #js
# Remove e retorna o elemento na posição 0 ("python")
print(liguangens.pop(0)) #python
